class Customer {
  final String name;
  final String id;
  final String email;
  final String phone;
  final String address;
  final String joinedDate;
  final int bookings;

  Customer({
    required this.name,
    required this.id,
    required this.email,
    required this.phone,
    required this.address,
    required this.joinedDate,
    required this.bookings,
  });
} 